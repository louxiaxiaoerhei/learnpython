# 变量: 一经赋值 可以重新赋值
# 常量: 一经赋值 不许重新赋值
# 表达式: 可以返回数值的称之为表达式

# 定义好了一个变量 记录年龄
age = 20
print("我今年的年龄是%d岁" % age)
# 又过了一年
age += 1 # age = age + 1
print("我今年的年龄是%d岁" % age)
# 再过了一年
age += 1 # age = age + 1
print("我今年的年龄是%d岁" % age)