# 如果提高函数的复用性 -> 有参数的函数
# 分析
"""
定义一个有参数的函数
def 函数名(形参1, 形参2, ...):
    代码逻辑
"""
def my_func(a, b):
    ret = a + b
    print(ret)

# 函数的调用: 函数名(实参1, 实参2, ...)
# 注意:实参和形参的个数需要一一对应 位置也需要一一对应
my_func(10, 20)
my_func(15, 25)
my_func(22, 33)



